extern const unsigned short tabSin[NB_SIN];

long mySin(unsigned short in) {
	unsigned short tmp = in % NB_SIN;
	switch (in / NB_SIN) {
	case 0: // 0°, 90°
		return (long)tabSin[tmp];
	case 1: // 90°, 180°
		return (long)tabSin[NB_SIN - 1 - tmp];
	case 2: // 180°, 270°
		return -(long)tabSin[tmp];
	default: // 270°, 360°
		return -(long)tabSin[NB_SIN - 1 - tmp];
	}
}

